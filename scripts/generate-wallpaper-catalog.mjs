import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDirectory, '..');
const wallpapersDirectory = path.join(projectRoot, 'src/assets/images/wallpapers');
const outputFile = path.join(projectRoot, 'src/app/generated/wallpaper-catalog.ts');

const supportedExtensions = new Map([
  ['.png', 'image'],
  ['.jpg', 'image'],
  ['.jpeg', 'image'],
  ['.webp', 'image'],
  ['.avif', 'image'],
  ['.mp4', 'video'],
  ['.webm', 'video'],
]);
const preferredDefaultWallpaperId = '【哲风壁纸】五条悟-咒术回战';

const entries = (await readdir(wallpapersDirectory, { withFileTypes: true }))
  .filter(entry => entry.isFile())
  .map(entry => {
    const extension = path.extname(entry.name).toLowerCase();
    const kind = supportedExtensions.get(extension);
    if (!kind) return null;

    const id = path.basename(entry.name, extension).normalize('NFC');
    const label = id
      .replace(/^【哲风壁纸】/, '')
      .replace(/[-_]+/g, ' ')
      .trim();

    return {
      id,
      label,
      assetUrl: `/assets/images/wallpapers/${encodeURIComponent(entry.name)}`,
      kind,
    };
  })
  .filter(Boolean);

const duplicateIds = entries
  .map(entry => entry.id.toLocaleLowerCase())
  .filter((id, index, ids) => ids.indexOf(id) !== index);

if (duplicateIds.length) {
  throw new Error(`Duplicate wallpaper ids: ${[...new Set(duplicateIds)].join(', ')}`);
}

if (!entries.length) {
  throw new Error('At least one supported wallpaper is required.');
}

const defaultWallpaperId = entries.some(entry => entry.id === preferredDefaultWallpaperId)
  ? preferredDefaultWallpaperId
  : (entries.find(entry => entry.kind === 'video') ?? entries[0]).id;
const staticFallbackWallpaperId = (entries.find(entry => entry.kind === 'image') ?? entries[0]).id;

entries.sort((left, right) => {
  if (left.id === defaultWallpaperId) return -1;
  if (right.id === defaultWallpaperId) return 1;
  return left.label.localeCompare(right.label, 'zh-CN', { numeric: true });
});

const generatedSource = `// This file is generated by scripts/generate-wallpaper-catalog.mjs.
// Add wallpaper files to src/assets/images/wallpapers, then restart npm run dev.
import type { WallpaperDefinition } from '../models/wallpaper';

export const GENERATED_DEFAULT_WALLPAPER_ID = ${JSON.stringify(defaultWallpaperId)};
export const GENERATED_STATIC_FALLBACK_WALLPAPER_ID = ${JSON.stringify(staticFallbackWallpaperId)};

export const GENERATED_WALLPAPERS: readonly WallpaperDefinition[] = ${JSON.stringify(entries, null, 2)};
`;

await mkdir(path.dirname(outputFile), { recursive: true });

let previousSource = '';
try {
  previousSource = await readFile(outputFile, 'utf8');
} catch {
  // The file is created on the first run.
}

if (previousSource !== generatedSource) {
  await writeFile(outputFile, generatedSource, 'utf8');
  console.log(`Generated ${entries.length} wallpaper entries.`);
} else {
  console.log(`Wallpaper catalog is current (${entries.length} entries).`);
}
